# Plik: len_type_demo.py

# Użycie len()
tekst = "Python"
lista = [10, 20, 30, 40]

print("Długość napisu:", len(tekst))  # Wynik: 6
print("Liczba elementów w liście:", len(lista))  # Wynik: 4

# Użycie type()
liczba = 42
napis = "Hello"
zbior = {1, 2, 3}

print("Typ zmiennej liczba:", type(liczba))  # Wynik: <class 'int'>
print("Typ zmiennej napis:", type(napis))  # Wynik: <class 'str'>
print("Typ zmiennej zbior:", type(zbior))  # Wynik: <class 'set'>
